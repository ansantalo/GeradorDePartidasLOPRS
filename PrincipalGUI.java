    private void mnuitmNovoTorneioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mnuitmNovoTorneioMousePressed
        ArrayList<String> nomeDosParticipantes = new ArrayList();
        
        try {
            nomeDosParticipantes = getNomeDosParticipantes();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Algo de errado com o arquivo de entrada.");
            return;
        }
        
        if (nomeDosParticipantes.size() > 0) {
            T = new Torneio();
            try {
                T.Lider = new Participante(nomeDosParticipantes.get(0));
                nomeDosParticipantes.remove(0);
                T.geraGrupos(nomeDosParticipantes);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                System.exit(0);
            }
            
            preenchePartidasNaTela(T);
            atualizaDesempenhoDosParticipantesAPartirDaInterfacePrimeiraFase(T);
            refreshTabelaGrupos(T);  
        }
    }//GEN-LAST:event_mnuitmNovoTorneioMousePressed

