    private void mnuitmGerarConfrontosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mnuitmGerarConfrontosMousePressed
        int dialogresult = JOptionPane.showConfirmDialog(null,"Isso vai gerar uma nova segunda fase de acordo com as pontuaes"
                + " da primeira fase.\nOs dados atuais na aba segunda fase (caso existam) sero apagados.\nTem certeza que quer criar"
                + " uma nova segunda fase?","No faa cagada.",JOptionPane.OK_CANCEL_OPTION);
        if (dialogresult == JOptionPane.YES_OPTION)
        {
            T.Eliminatorias = new Eliminatoria(
                    T.nDeGrupos >= 4 ? TipoEliminatorias.OITO_PARTICIPANTES : TipoEliminatorias.QUATRO_PARTICIPANTES,
                    T.passaramParaSegundaFase(),
                    new ParticipanteEliminatoria(T.Lider,"",false));
            limpaCamposTextuaisEliminatorias();
            exibeSegundaFaseNaTela();
            atualizaDesempenhoDosParticipantesAPartirDaInterfaceSegundaFase(T);
            refreshEliminatorias(T);
        }
    }//GEN-LAST:event_mnuitmGerarConfrontosMousePressed

