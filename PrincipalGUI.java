    private void adicionaPartidaNoDesempenho(Participante A, Participante B, String saldoAString, String saldoBString) {
        int saldoA = -1;
        try {
            saldoA = Integer.parseInt(saldoAString);
        }
        catch (Exception e) {} 
        
        int saldoB = -1;
        try {
            saldoB = Integer.parseInt(saldoBString);
        }
        catch (Exception e) {} 
        
        if (saldoA == -1 || saldoB == -1)
            return;
        
        if ((saldoA == 0 || saldoB == 0) && (saldoA != saldoB)) {
            A.jogos++;
            B.jogos++;
            
            if (saldoA > saldoB) {
                A.vitorias++;
                A.saldo += saldoA;
                B.saldo -= saldoA;
            }
            else {
                B.vitorias++;
                B.saldo += saldoB;
                A.saldo -= saldoB;
            }
        }
        else {
            return;
        }
    }
    
